"""
tests/unit/test_stix_validator.py
==================================
Unit tests for the STIX 2.1 pattern validator (gnat.stix.pattern_validator).

Tests cover:
1.  ValidationResult dataclass (bool coercion, repr)
2.  PatternValidationError attributes
3.  Valid patterns — all IOC types generated by GNAT
4.  Invalid patterns — structural errors (no brackets, empty, trailing junk)
5.  Invalid patterns — bad object type
6.  Invalid patterns — bad comparison operator
7.  Invalid patterns — unquoted string value
8.  Invalid patterns — incomplete expressions (missing value, missing path)
9.  Compound expressions — AND / OR
10. Property path variants — dot notation, quoted component, index notation
11. IN list values
12. WITHIN / REPEATEDWITHIN qualifiers
13. Parenthesised sub-expressions
14. x-* extension type warnings
15. PatternValidator class — raise_on_error, allow_custom_types
16. validate_pattern() convenience function
17. Indicator.__init__ validate= kwarg (integration with ORM)
18. CLI — gnat validate pattern / bundle subcommands
"""

from __future__ import annotations

import json

import pytest

from gnat.stix.pattern_validator import (
    PatternValidationError,
    PatternValidator,
    ValidationResult,
    validate_pattern,
)

# ---------------------------------------------------------------------------
# 1. ValidationResult
# ---------------------------------------------------------------------------


class TestValidationResult:
    def test_bool_true_when_valid(self):
        r = ValidationResult(valid=True)
        assert bool(r) is True
        assert r

    def test_bool_false_when_invalid(self):
        r = ValidationResult(valid=False, errors=["bad"])
        assert bool(r) is False
        assert not r

    def test_repr_valid(self):
        r = ValidationResult(valid=True)
        assert "valid" in repr(r)

    def test_repr_invalid_shows_count(self):
        r = ValidationResult(valid=False, errors=["e1", "e2"])
        assert "2 error" in repr(r)

    def test_strict_flag(self):
        r = ValidationResult(valid=True, strict=True)
        assert r.strict is True


# ---------------------------------------------------------------------------
# 2. PatternValidationError
# ---------------------------------------------------------------------------


class TestPatternValidationError:
    def test_attributes(self):
        err = PatternValidationError("[bad", ["Missing ']'"])
        assert err.pattern == "[bad"
        assert "Missing ']'" in err.errors
        assert isinstance(err, ValueError)

    def test_str_contains_summary(self):
        err = PatternValidationError("[x", ["Missing ']'"])
        assert "Invalid STIX pattern" in str(err)


