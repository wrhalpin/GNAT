VALID_PATTERNS = [
    "[ipv4-addr:value = '1.2.3.4']",
    "[ipv4-addr:value = '192.168.1.0/24']",
    "[ipv6-addr:value = '::1']",
    "[domain-name:value = 'evil.com']",
    "[url:value = 'https://evil.com/path?q=1']",
    "[file:hashes.MD5 = 'abc123def456']",
    "[file:hashes.SHA-1 = 'aabbccddeeff']",
    "[file:hashes.SHA-256 = 'deadbeefdeadbeefdeadbeefdeadbeef']",
    "[email-addr:value = 'attacker@evil.com']",
    "[email-message:subject = 'You have won']",
    "[windows-registry-key:key = 'HKLM\\\\Software\\\\evil']",
    "[mutex:name = 'Global\\\\evil_mutex']",
    "[file:name = 'malware.exe']",
    "[autonomous-system:number = 12345]",
    "[software:name = 'Evil Tool']",
    "[user-account:user_id = 'admin']",
    "[network-traffic:dst_ref.type = 'ipv4-addr' AND network-traffic:dst_ref.value = '1.2.3.4']",
    "[process:name = 'cmd.exe']",
    "[mac-addr:value = 'AA:BB:CC:DD:EE:FF']",
]


class TestValidPatterns:
    @pytest.mark.parametrize("pattern", VALID_PATTERNS)
    def test_valid(self, pattern):
        result = validate_pattern(pattern)
        assert result.valid, f"Expected valid but got errors: {result.errors} for {pattern!r}"

    def test_double_quoted_string(self):
        result = validate_pattern('[domain-name:value = "evil.com"]')
        assert result.valid

    def test_integer_value(self):
        result = validate_pattern("[autonomous-system:number = 64496]")
        assert result.valid

    def test_float_value(self):
        result = validate_pattern("[network-traffic:dst_port = 443]")
        assert result.valid

    def test_boolean_true_value(self):
        result = validate_pattern("[process:is_hidden = true]")
        assert result.valid

    def test_boolean_false_value(self):
        result = validate_pattern("[process:is_hidden = false]")
        assert result.valid

    def test_null_value(self):
        result = validate_pattern("[process:parent_ref = null]")
        assert result.valid

    def test_extra_whitespace(self):
        result = validate_pattern("  [ ipv4-addr : value = '1.2.3.4' ]  ")
        assert result.valid


