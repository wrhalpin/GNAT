# 15. PatternValidator class
# ---------------------------------------------------------------------------


class TestPatternValidator:
    def test_valid_returns_result(self):
        v = PatternValidator()
        r = v.validate("[ipv4-addr:value = '1.2.3.4']")
        assert r.valid

    def test_invalid_returns_result_by_default(self):
        v = PatternValidator()
        r = v.validate("[bad")
        assert not r.valid

    def test_raise_on_error(self):
        v = PatternValidator(raise_on_error=True)
        with pytest.raises(PatternValidationError) as exc_info:
            v.validate("[bad")
        assert "[bad" in exc_info.value.pattern

    def test_raise_on_error_passes_valid(self):
        v = PatternValidator(raise_on_error=True)
        r = v.validate("[ipv4-addr:value = '1.2.3.4']")
        assert r.valid

    def test_strict_no_stix2patterns_falls_back(self):
        # stix2-patterns not installed; strict falls back to tier-1
        v = PatternValidator(strict=True)
        r = v.validate("[ipv4-addr:value = '1.2.3.4']")
        assert r.valid
        assert r.strict is False  # stix2-patterns not installed


