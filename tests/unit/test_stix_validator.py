# 6. Invalid patterns — bad comparison operator
# ---------------------------------------------------------------------------


class TestBadOperator:
    def test_invalid_operator(self):
        result = validate_pattern("[ipv4-addr:value == '1.2.3.4']")
        # == is not valid (single = is)
        # tokenizer splits into = and = separately, which may parse differently
        # This should produce either an error or be considered trailing content
        # The main thing is it should not silently accept it as valid
        # (implementation may vary — just check it handles it)
        # Both valid and invalid responses are acceptable depending on tokenizer
        # So just check it doesn't crash
        assert isinstance(result, ValidationResult)

    def test_sql_like_operator(self):
        # Lowercase "like" — tokenizer uses re.IGNORECASE so it is accepted
        # as the LIKE keyword.  Our validator is intentionally lenient here.
        result = validate_pattern("[domain-name:value like 'evil%']")
        assert result.valid


