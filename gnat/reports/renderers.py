class DOCXRenderer:
    """Renders a ReportDocument to DOCX using python-docx (pure Python)."""

    _PRIMARY = "1A3A5C"
    _ACCENT  = "2E75B6"
    _MUTED   = "6B7280"
    _WHITE   = "FFFFFF"
    _ALT_ROW = "F5F7FA"

    def render(self, doc: "ReportDocument", path: str) -> str:
        try:
            from docx import Document
            from docx.shared import Pt, RGBColor
            from docx.enum.text import WD_ALIGN_PARAGRAPH
        except ImportError:
            raise ImportError(
                "python-docx required for DOCX output: "
                "pip install 'gnat[reports]'"
            )

        Path(path).parent.mkdir(parents=True, exist_ok=True)
        d = Document()

        # Page margins (1 inch all around)
        for section in d.sections:
            section.top_margin    = self._pt(72)
            section.bottom_margin = self._pt(72)
            section.left_margin   = self._pt(72)
            section.right_margin  = self._pt(72)

        # Title
        h1 = d.add_heading(doc.title, level=1)
        h1.runs[0].font.color.rgb = self._rgb(self._PRIMARY)

        # Metadata lines
        self._meta_line(d, "Report Type", doc.report_type.title())
        self._meta_line(d, "Generated",
                        doc.generated_at.strftime("%Y-%m-%d %H:%M UTC"))
        self._meta_line(d, "Period",
                        f"{doc.period_start.strftime('%Y-%m-%d')} to "
                        f"{doc.period_end.strftime('%Y-%m-%d')}")
        if doc.config and doc.config.sectors:
            self._meta_line(d, "Sectors", ", ".join(doc.config.sectors[:6]))

        d.add_paragraph()  # spacer

        # Sections
        for sec in doc.sections:
            h2 = d.add_heading(sec.title, level=2)
            h2.runs[0].font.color.rgb = self._rgb(self._PRIMARY)

            if sec.has_narrative:
                for para in sec.narrative.split("\n\n"):
                    if para.strip():
                        p = d.add_paragraph(para.strip())
                        p.paragraph_format.space_after = self._pt(6)

            self._add_data_tables(d, sec)

        # Footer paragraph
        footer = d.add_paragraph()
        footer.paragraph_format.space_before = self._pt(24)
        footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = footer.add_run("Generated by GNAT Threat Intelligence Platform")
        run.font.size = Pt(9)
        run.font.italic = True
        run.font.color.rgb = self._rgb(self._MUTED)

        d.save(path)
        logger.info("DOCXRenderer: wrote %s", path)
        return path

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _meta_line(self, d, label: str, value: str) -> None:
        from docx.shared import Pt
        p = d.add_paragraph()
        p.paragraph_format.space_after = self._pt(2)
        bold_run = p.add_run(f"{label}: ")
        bold_run.bold = True
        bold_run.font.size = Pt(9)
        bold_run.font.color.rgb = self._rgb(self._MUTED)
        val_run = p.add_run(value)
        val_run.font.size = Pt(9)
        val_run.font.color.rgb = self._rgb(self._MUTED)

    def _add_data_tables(self, d, section: "ReportSection") -> None:
        data = section.data
        if "top_actors" in data and data["top_actors"]:
            rows = [
                [a.get("name", ""), ", ".join(a.get("motivation", []))[:60]]
                for a in data["top_actors"][:8]
            ]
            self._table(d, ["Threat Actor", "Motivation"], rows)

        if "critical_vulns" in data and data["critical_vulns"]:
            rows = [
                [v.get("cve_id") or v.get("name", ""),
                 str(v.get("cvss", "N/A")),
                 "Yes" if v.get("exploited") else "No"]
                for v in data["critical_vulns"][:10]
            ]
            self._table(d, ["CVE / Name", "CVSS", "Exploited"], rows)

        if "ioc_by_type" in data and data["ioc_by_type"]:
            rows = [[k, str(v)] for k, v in list(data["ioc_by_type"].items())[:8]]
            self._table(d, ["IOC Type", "Count"], rows)

        if "sector_distribution" in data and data["sector_distribution"]:
            rows = [[k, str(v)]
                    for k, v in list(data["sector_distribution"].items())[:8]]
            self._table(d, ["Sector", "Object Count"], rows)

        if "source_breakdown" in data and data["source_breakdown"]:
            rows = [[k, str(v)]
                    for k, v in list(data["source_breakdown"].items())[:8]]
            self._table(d, ["Source", "Objects"], rows)

    def _table(self, d, headers: List[str], rows: List[List[str]]) -> None:
        from docx.shared import Pt
        from docx.oxml.ns import qn
        from docx.oxml import OxmlElement

        tbl = d.add_table(rows=1 + len(rows), cols=len(headers))
        tbl.style = "Table Grid"

        # Header row
        hdr_cells = tbl.rows[0].cells
        for i, h in enumerate(headers):
            hdr_cells[i].text = h
            _set_cell_bg(hdr_cells[i], self._PRIMARY)
            run = hdr_cells[i].paragraphs[0].runs[0]
            run.bold = True
            run.font.color.rgb = self._rgb(self._WHITE)
            run.font.size = Pt(9)

        # Data rows
        for r_idx, row in enumerate(rows):
            cells = tbl.rows[r_idx + 1].cells
            bg = self._ALT_ROW if r_idx % 2 else self._WHITE
            for c_idx, val in enumerate(row):
                cells[c_idx].text = str(val)
                _set_cell_bg(cells[c_idx], bg)
                cells[c_idx].paragraphs[0].runs[0].font.size = Pt(9)

        d.add_paragraph()  # spacer after table

    @staticmethod
    def _rgb(hex6: str) -> "RGBColor":
        from docx.shared import RGBColor
        r, g, b = int(hex6[0:2], 16), int(hex6[2:4], 16), int(hex6[4:6], 16)
        return RGBColor(r, g, b)

    @staticmethod
    def _pt(points: float) -> "Pt":
        from docx.shared import Pt
        return Pt(points)


def _set_cell_bg(cell, hex6: str) -> None:
    """Set table cell background colour via direct OOXML (python-docx omits this)."""
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement
    tc = cell._tc
    tcp = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hex6)
    tcp.append(shd)


