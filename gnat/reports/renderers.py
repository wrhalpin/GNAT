"""
gnat.reports.renderers
==========================

Four report renderers — Markdown, HTML, PDF, DOCX — all consuming the
same :class:`~gnat.reports.base.ReportDocument` intermediate structure.

Architecture
------------
Markdown is the canonical intermediate format.  HTML is generated from
it using a lightweight template (no external Markdown parser required —
the document structure is simple enough to generate HTML directly).
PDF is generated via ``reportlab`` (stdlib-compatible, no subprocess).
DOCX is generated via a Node.js script using the ``docx`` npm package
(following the skill recommendations).

All renderers write to a file path and return that path.
"""

