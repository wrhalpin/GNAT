class HTMLRenderer:
    """Renders a ReportDocument to a self-contained HTML file."""

    def render(self, doc: "ReportDocument", path: str) -> str:
        html = self._build(doc)
        Path(path).write_text(html, encoding="utf-8")
        logger.info("HTMLRenderer: wrote %s", path)
        return path

