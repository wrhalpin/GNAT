class DOCXRenderer:
    """Renders a ReportDocument to DOCX using docx (Node.js npm package)."""

    def render(self, doc: "ReportDocument", path: str) -> str:
        Path(path).parent.mkdir(parents=True, exist_ok=True)
        script = self._build_script(doc, path)
        with tempfile.NamedTemporaryFile(
            suffix=".js", delete=False, mode="w", encoding="utf-8"
        ) as f:
            f.write(script)
            script_path = f.name

        try:
            result = subprocess.run(
                ["node", script_path],
                capture_output=True,
                text=True,
                timeout=60,
            )
            if result.returncode != 0:
                raise RuntimeError(
                    f"DOCX generation failed: {result.stderr[:500]}"
                )
        finally:
            os.unlink(script_path)

        logger.info("DOCXRenderer: wrote %s", path)
        return path

    def _build_script(self, doc: "ReportDocument", out_path: str) -> str:
        sections_js = ",\n".join(
            self._section_to_js(s) for s in doc.sections
        )
        sector_text = (
            "Sectors: " + ", ".join(doc.config.sectors[:6])
            if doc.config and doc.config.sectors else ""
        )
        title      = _js_str(doc.title)
        gen_date   = _js_str(doc.generated_at.strftime("%Y-%m-%d %H:%M UTC"))
        period     = _js_str(
            f"{doc.period_start.strftime('%Y-%m-%d')} to "
            f"{doc.period_end.strftime('%Y-%m-%d')}"
        )
        sector_js  = _js_str(sector_text)
        out_js     = _js_str(str(Path(out_path).resolve()))

        return f"""
const {{ Document, Packer, Paragraph, TextRun, Table, TableRow,
         TableCell, HeadingLevel, AlignmentType, BorderStyle,
         WidthType, ShadingType, LevelFormat }} = require('docx');
const fs = require('fs');

const PRIMARY = "1A3A5C";
const ACCENT  = "2E75B6";
const BORDER  = {{ style: BorderStyle.SINGLE, size: 1, color: "D0D7DE" }};
const BORDERS = {{ top: BORDER, bottom: BORDER, left: BORDER, right: BORDER }};
const CELL_W  = {{ size: 4680, type: WidthType.DXA }};

function header(text, level) {{
  return new Paragraph({{
    heading: level,
    children: [new TextRun({{ text, bold: true, color: PRIMARY,
      size: level === HeadingLevel.HEADING_1 ? 36 : 28 }})]
  }});
}}

function body(text) {{
  return text.split("\\n\\n").filter(p => p.trim()).map(p =>
    new Paragraph({{ children: [new TextRun({{ text: p.trim(), size: 20 }})],
      spacing: {{ after: 120 }} }})
  );
}}

function hr() {{
  return new Paragraph({{
    border: {{ bottom: {{ style: BorderStyle.SINGLE, size: 6, color: ACCENT, space: 1 }} }},
    spacing: {{ after: 160 }}
  }});
}}

function metaLine(label, value) {{
  return new Paragraph({{
    children: [
      new TextRun({{ text: label + ": ", bold: true, size: 18, color: "6B7280" }}),
      new TextRun({{ text: value, size: 18, color: "6B7280" }})
    ],
    spacing: {{ after: 40 }}
  }});
}}

function simpleTable(rows, headers) {{
  return new Table({{
    width: {{ size: 9360, type: WidthType.DXA }},
    columnWidths: Array(headers.length).fill(Math.floor(9360 / headers.length)),
    rows: [
      new TableRow({{
        children: headers.map(h => new TableCell({{
          borders: BORDERS,
          width: CELL_W,
          margins: {{ top: 80, bottom: 80, left: 120, right: 120 }},
          shading: {{ fill: PRIMARY, type: ShadingType.CLEAR }},
          children: [new Paragraph({{ children: [new TextRun({{
            text: h, bold: true, color: "FFFFFF", size: 18
          }})] }})]
        }}))
      }}),
      ...rows.map((row, i) => new TableRow({{
        children: row.map(cell => new TableCell({{
          borders: BORDERS,
          width: CELL_W,
          margins: {{ top: 80, bottom: 80, left: 120, right: 120 }},
          shading: {{ fill: i % 2 === 0 ? "FFFFFF" : "F5F7FA", type: ShadingType.CLEAR }},
          children: [new Paragraph({{ children: [new TextRun({{
            text: String(cell), size: 18
          }})] }})]
        }}))
      }}))
    ]
  }});
}}

const children = [
  header({title}, HeadingLevel.HEADING_1),
  metaLine("Report Type", {_js_str(doc.report_type.title())}),
  metaLine("Generated", {gen_date}),
  metaLine("Period", {period}),
  {f'metaLine("Sectors", {sector_js}),' if sector_text else ''}
  hr(),
  {sections_js}
  new Paragraph({{
    children: [new TextRun({{ text: "Generated by GNAT Threat Intelligence Platform",
      size: 16, color: "9CA3AF", italics: true }})],
    alignment: AlignmentType.CENTER,
    spacing: {{ before: 400 }}
  }})
];

const doc = new Document({{
  styles: {{
    default: {{ document: {{ run: {{ font: "Arial", size: 20 }} }} }},
    paragraphStyles: [
      {{ id: "Heading1", name: "Heading 1", basedOn: "Normal", next: "Normal", quickFormat: true,
        run: {{ size: 36, bold: true, font: "Arial" }},
        paragraph: {{ spacing: {{ before: 240, after: 240 }}, outlineLevel: 0 }} }},
      {{ id: "Heading2", name: "Heading 2", basedOn: "Normal", next: "Normal", quickFormat: true,
        run: {{ size: 28, bold: true, font: "Arial" }},
        paragraph: {{ spacing: {{ before: 180, after: 120 }}, outlineLevel: 1 }} }},
    ]
  }},
  sections: [{{
    properties: {{
      page: {{
        size: {{ width: 12240, height: 15840 }},
        margin: {{ top: 1440, right: 1440, bottom: 1440, left: 1440 }}
      }}
    }},
    children
  }}]
}});

Packer.toBuffer(doc).then(buf => {{
  fs.writeFileSync({out_js}, buf);
  console.log("DOCX written:", {out_js});
}}).catch(err => {{ console.error(err); process.exit(1); }});
"""

    def _section_to_js(self, section: "ReportSection") -> str:
        parts = [
            f"  header({_js_str(section.title)}, HeadingLevel.HEADING_2),"
        ]

        if section.has_narrative:
            parts.append(f"  ...body({_js_str(section.narrative)}),")

        # Simple data table for top-N lists
        data = section.data
        if "top_actors" in data and data["top_actors"]:
            rows = [
                [a.get("name", ""), ", ".join(a.get("motivation", []))[:40]]
                for a in data["top_actors"][:8]
            ]
            rows_js = json.dumps(rows)
            parts.append(
                f"  simpleTable({rows_js}, ['Threat Actor', 'Motivation']),"
            )
        elif "critical_vulns" in data and data["critical_vulns"]:
            rows = [
                [
                    v.get("cve_id") or v.get("name", ""),
                    str(v.get("cvss", "N/A")),
                    "Yes" if v.get("exploited") else "No",
                ]
                for v in data["critical_vulns"][:10]
            ]
            rows_js = json.dumps(rows)
            parts.append(
                f"  simpleTable({rows_js}, ['CVE', 'CVSS', 'Exploited']),"
            )
        elif "ioc_by_type" in data and data["ioc_by_type"]:
            rows = [[k, str(v)] for k, v in list(data["ioc_by_type"].items())[:8]]
            rows_js = json.dumps(rows)
            parts.append(f"  simpleTable({rows_js}, ['IOC Type', 'Count']),")

        return "\n".join(parts)


