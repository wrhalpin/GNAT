class MarkdownRenderer:
    """Renders a ReportDocument to Markdown."""

    def render(self, doc: "ReportDocument", path: str) -> str:
        md = self._build(doc)
        Path(path).write_text(md, encoding="utf-8")
        logger.info("MarkdownRenderer: wrote %s", path)
        return path

    def render_string(self, doc: "ReportDocument") -> str:
        return self._build(doc)

    def _build(self, doc: "ReportDocument") -> str:
        lines = []
        lines.append(f"# {doc.title}")
        lines.append(f"")
        lines.append(f"**Report Type:** {doc.report_type.title()}  ")
        lines.append(f"**Generated:** {doc.generated_at.strftime('%Y-%m-%d %H:%M UTC')}  ")
        lines.append(f"**Period:** {doc.period_start.strftime('%Y-%m-%d')} "
                     f"to {doc.period_end.strftime('%Y-%m-%d')}  ")
        if doc.config and doc.config.sectors:
            lines.append(f"**Sectors:** {', '.join(doc.config.sectors)}  ")
        lines.append("")
        lines.append("---")
        lines.append("")

        for section in doc.sections:
            lines.append(f"## {section.title}")
            lines.append("")

            if section.has_narrative:
                lines.append(section.narrative)
                lines.append("")

            if section.has_data:
                for sub in _render_data_md(section):
                    lines.append(sub)
                lines.append("")

        lines.append("---")
        lines.append(f"*Generated by GNAT threat intelligence platform*")

        return "\n".join(lines)


