"""
gnat.reports.delivery
=========================

Report delivery targets — email (SMTP) and SharePoint (via DirectLine/Graph).

Both implement the :class:`~gnat.export.base.ExportDelivery` interface so
they can be used with the existing export pipeline as well as the report
generator.

INI configuration
-----------------
::

    [email]
    smtp_host     = smtp.office365.com
    smtp_port     = 587
    smtp_user     = reports@example.com
    smtp_password = <password>
    smtp_tls      = true
    from_address  = gnat-reports@example.com

    [sharepoint]
    # Uses the Copilot DirectLine secret if available, else requires
    # a separate Graph API app registration
    directline_secret = <secret>
    tenant_id         = <azure-tenant-id>
    client_id         = <app-client-id>
    client_secret     = <app-secret>
"""

from __future__ import annotations

import logging
import mimetypes
import smtplib
import ssl
from email.mime.application import MIMEApplication
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# EmailDelivery
# ---------------------------------------------------------------------------


class EmailDelivery:
    """
    Sends report files as email attachments via SMTP.

    Parameters
    ----------
    smtp_host : str
    smtp_port : int
    smtp_user : str
    smtp_password : str
    from_address : str
    to_addresses : list of str
    subject : str
    body_html : str, optional
        HTML body.  Plain-text fallback is auto-generated.
    use_tls : bool
        Start TLS after connect.  Default ``True``.

    Examples
    --------
    ::

        delivery = EmailDelivery.from_ini(
            to_addresses=["soc@example.com"],
            subject="Daily Threat Intel — 2024-03-21",
        )
        delivery.send(file_paths=["report.pdf", "report.html"])
    """

    def __init__(
        self,
        smtp_host: str,
        smtp_port: int,
        smtp_user: str,
        smtp_password: str,
        from_address: str,
        to_addresses: list[str],
        subject: str,
        body_html: str = "",
        use_tls: bool = True,
    ):
        self._host = smtp_host
        self._port = smtp_port
        self._user = smtp_user
        self._password = smtp_password
        self._from = from_address
        self._to = to_addresses
        self._subject = subject
        self._body_html = body_html
        self._tls = use_tls

    @classmethod
    def from_ini(
        cls,
        to_addresses: list[str],
        subject: str,
        body_html: str = "",
        config_path: str | None = None,
    ) -> EmailDelivery:
        """Load SMTP settings from the ``[email]`` INI section."""
        from gnat.config import GNATConfig

        cfg = GNATConfig(config_path)
        try:
            s = cfg.get("email")
        except KeyError:
            raise KeyError(
                "No [email] section in config.ini. Add:\n"
                "  [email]\n"
                "  smtp_host = smtp.office365.com\n"
                "  smtp_port = 587\n"
                "  smtp_user = user@example.com\n"
                "  smtp_password = <password>\n"
                "  from_address = reports@example.com\n"
            )
        return cls(
            smtp_host=s.get("smtp_host", "localhost"),
            smtp_port=int(s.get("smtp_port", 587)),
            smtp_user=s.get("smtp_user", ""),
            smtp_password=s.get("smtp_password", ""),
            from_address=s.get("from_address", s.get("smtp_user", "")),
            to_addresses=to_addresses,
            subject=subject,
            body_html=body_html,
            use_tls=s.get("smtp_tls", "true").lower() == "true",
        )

    def send(self, file_paths: list[str]) -> dict[str, Any]:
        """
        Send report files as attachments.

        Parameters
        ----------
        file_paths : list of str
            Local paths of files to attach.

        Returns
        -------
        dict
            ``{"success": bool, "recipients": [...], "error": str or None}``
        """
        msg = MIMEMultipart("mixed")
        msg["From"] = self._from
        msg["To"] = ", ".join(self._to)
        msg["Subject"] = self._subject

        # Body
        body_text = "Please find the attached threat intelligence report.\n\nGenerated by GNAT."
        alt = MIMEMultipart("alternative")
        alt.attach(MIMEText(body_text, "plain"))
        if self._body_html:
            alt.attach(MIMEText(self._body_html, "html"))
        msg.attach(alt)

        # Attachments
        attached = []
        for fp in file_paths:
            p = Path(fp)
            if not p.exists():
                logger.warning("EmailDelivery: file not found: %s", fp)
                continue
            mime_type, _ = mimetypes.guess_type(str(p))
            _, _ = (mime_type or "application/octet-stream").split("/", 1)
            with open(p, "rb") as f:
                part = MIMEApplication(f.read(), Name=p.name)
                part["Content-Disposition"] = f'attachment; filename="{p.name}"'
                msg.attach(part)
                attached.append(p.name)

        try:
            if self._tls:
                context = ssl.create_default_context()
                with smtplib.SMTP(self._host, self._port) as server:
                    server.ehlo()
                    server.starttls(context=context)
                    server.login(self._user, self._password)
                    server.sendmail(self._from, self._to, msg.as_string())
            else:
                with smtplib.SMTP(self._host, self._port) as server:
                    server.login(self._user, self._password)
                    server.sendmail(self._from, self._to, msg.as_string())

            logger.info(
                "EmailDelivery: sent to %s with %d attachment(s): %s",
                self._to,
                len(attached),
                attached,
            )
            return {"success": True, "recipients": self._to, "error": None, "attachments": attached}

        except smtplib.SMTPException as exc:
            logger.error("EmailDelivery: SMTP error — %s", exc)
            return {"success": False, "recipients": self._to, "error": str(exc), "attachments": []}


