class SharePointDelivery:
    """
    Uploads report files to a SharePoint document library via the
    Microsoft Graph API.

    Two authentication modes:

    **Client credentials** (server-to-server, recommended for automation):
    App registration in Azure AD with ``Files.ReadWrite.All`` permission.
    Configure ``tenant_id``, ``client_id``, and ``client_secret`` in INI.

    **DirectLine proxy** (reuses Copilot infrastructure):
    Uses the Bot Framework DirectLine connection from ``[copilot]``
    to instruct Copilot to save files. Simpler to configure but relies
    on the Copilot bot having write access to SharePoint.

    Parameters
    ----------
    site_url : str
        Full SharePoint site URL, e.g.
        ``"https://contoso.sharepoint.com/sites/Security"``.
    library_path : str
        Library name or relative path within the site, e.g.
        ``"Threat Reports"``.
    tenant_id : str, optional
        Azure tenant id for client-credentials auth.
    client_id : str, optional
        App registration client id.
    client_secret : str, optional
        App registration client secret.
    folder : str, optional
        Subfolder within the library to upload to.
        Auto-created if it does not exist.

    Examples
    --------
    ::

        sp = SharePointDelivery.from_ini(
            site_url="https://contoso.sharepoint.com/sites/Security",
            library_path="Threat Reports",
        )
        result = sp.upload(file_paths=["report.pdf", "report.docx"])
    """

    def __init__(
        self,
        site_url: str,
        library_path: str,
        tenant_id: str = "",
        client_id: str = "",
        client_secret: str = "",
        folder: str = "",
    ):
        self._site_url = site_url.rstrip("/")
        self._library_path = library_path
        self._tenant_id = tenant_id
        self._client_id = client_id
        self._client_secret = client_secret
        self._folder = folder

    @classmethod
    def from_ini(
        cls,
        site_url: str,
        library_path: str,
        folder: str = "",
        config_path: str | None = None,
    ) -> SharePointDelivery:
        """Load Graph API credentials from the ``[sharepoint]`` INI section."""
        from gnat.config import GNATConfig

        cfg = GNATConfig(config_path)
        try:
            s = cfg.get("sharepoint")
        except KeyError:
            raise KeyError(
                "No [sharepoint] section in config.ini. Add:\n"
                "  [sharepoint]\n"
                "  tenant_id     = <azure-tenant-id>\n"
                "  client_id     = <app-client-id>\n"
                "  client_secret = <app-secret>\n"
            )
        return cls(
            site_url=site_url,
            library_path=library_path,
            tenant_id=s.get("tenant_id", ""),
            client_id=s.get("client_id", ""),
            client_secret=s.get("client_secret", ""),
            folder=folder,
        )

    def upload(self, file_paths: list[str]) -> dict[str, Any]:
        """
        Upload files to SharePoint.

        Parameters
        ----------
        file_paths : list of str
            Local file paths to upload.

        Returns
        -------
        dict
            ``{"success": bool, "uploaded": [...], "urls": [...], "error": str or None}``
        """
        token = self._get_access_token()
        if not token:
            return {
                "success": False,
                "uploaded": [],
                "urls": [],
                "error": "Failed to obtain access token",
            }

        site_id = self._get_site_id(token)
        if not site_id:
            return {
                "success": False,
                "uploaded": [],
                "urls": [],
                "error": f"Could not resolve site id for {self._site_url}",
            }

        drive_id = self._get_drive_id(token, site_id)
        if not drive_id:
            return {
                "success": False,
                "uploaded": [],
                "urls": [],
                "error": "Could not find document library drive",
            }

        uploaded = []
        urls = []
        errors = []

        for fp in file_paths:
            p = Path(fp)
            if not p.exists():
                errors.append(f"File not found: {fp}")
                continue
            try:
                url = self._upload_file(token, drive_id, p)
                if url:
                    uploaded.append(p.name)
                    urls.append(url)
                    logger.info("SharePointDelivery: uploaded %s → %s", p.name, url)
                else:
                    errors.append(f"Upload returned no URL for {p.name}")
            except Exception as exc:
                errors.append(f"{p.name}: {exc}")
                logger.error("SharePointDelivery: upload error for %s — %s", p.name, exc)

        success = len(uploaded) > 0 and not errors
        return {
            "success": success,
            "uploaded": uploaded,
            "urls": urls,
            "error": "; ".join(errors) if errors else None,
        }

    # ── Graph API helpers ──────────────────────────────────────────────────

    def _get_access_token(self) -> str | None:
        """Obtain an OAuth2 client-credentials token from Azure AD."""
        import urllib.parse
        import urllib.request

        if not all([self._tenant_id, self._client_id, self._client_secret]):
            logger.error("SharePointDelivery: missing tenant_id/client_id/client_secret")
            return None

        url = f"https://login.microsoftonline.com/{self._tenant_id}/oauth2/v2.0/token"
        data = urllib.parse.urlencode(
            {
                "grant_type": "client_credentials",
                "client_id": self._client_id,
                "client_secret": self._client_secret,
                "scope": "https://graph.microsoft.com/.default",
            }
        ).encode()

        try:
            import json as _json

            req = urllib.request.Request(url, data=data, method="POST")
            with urllib.request.urlopen(req, timeout=30) as resp:  # nosec B310  # nosemgrep
                result = _json.loads(resp.read())
                return result.get("access_token")
        except Exception as exc:
            logger.error("SharePointDelivery: token error — %s", exc)  # nosemgrep
            return None

    def _graph_get(self, token: str, path: str) -> dict | None:
        import json as _json
        import urllib.request

        url = f"https://graph.microsoft.com/v1.0{path}"
        req = urllib.request.Request(
            url,
            headers={"Authorization": f"Bearer {token}"},
        )
        try:
            with urllib.request.urlopen(req, timeout=30) as resp:  # nosec B310  # nosemgrep
                return _json.loads(resp.read())
        except Exception as exc:
            logger.debug("SharePointDelivery: GET %s → %s", path, exc)
            return None

    def _get_site_id(self, token: str) -> str | None:
        """Resolve SharePoint site URL to a Graph site id."""
        from urllib.parse import urlparse

        parsed = urlparse(self._site_url)
        hostname = parsed.netloc
        path = parsed.path.strip("/")
        result = self._graph_get(token, f"/sites/{hostname}:/{path}")
        return result.get("id") if result else None

    def _get_drive_id(self, token: str, site_id: str) -> str | None:
        """Get the document library drive id."""
        result = self._graph_get(token, f"/sites/{site_id}/drives")
        if not result:
            return None
        drives = result.get("value", [])
        # Find drive matching library_path
        for drive in drives:
            if drive.get("name", "").lower() == self._library_path.lower() or drive.get(
                "webUrl", ""
            ).endswith(self._library_path.replace(" ", "%20")):
                return drive["id"]
        # Fall back to default drive
        return drives[0]["id"] if drives else None

    def _upload_file(self, token: str, drive_id: str, file_path: Path) -> str | None:
        """Upload one file via Graph DriveItem PUT."""
        import urllib.request

        dest_folder = self._folder or ""
        if dest_folder:
            item_path = f"/{dest_folder}/{file_path.name}"
        else:
            item_path = f"/{file_path.name}"

        url = f"https://graph.microsoft.com/v1.0/drives/{drive_id}/root:{item_path}:/content"
        data = file_path.read_bytes()
        mime, _ = mimetypes.guess_type(str(file_path))
        req = urllib.request.Request(
            url,
            data=data,
            headers={
                "Authorization": f"Bearer {token}",
                "Content-Type": mime or "application/octet-stream",
            },
            method="PUT",
        )
        try:
            import json as _json

            with urllib.request.urlopen(req, timeout=120) as resp:  # nosec B310  # nosemgrep
                result = _json.loads(resp.read())
                return result.get("webUrl")
        except Exception as exc:
            raise RuntimeError(f"Graph upload failed: {exc}") from exc
